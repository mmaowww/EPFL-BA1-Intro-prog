public class Codes
{
	public static void main(String [] args)
	{
		int star;
		int neuf;
		int vDouble;

		star = '*';
		neuf = '9';
		vDouble = 'W';

		System.out.println("Le caractère qui correspond à *: " + star);
		System.out.println("Le caractère qui correspond à 9: " + neuf);
		System.out.println("Le caractère qui correspond à W: " + vDouble);
	
		char trenteQuatre;
		char soixanteCinq;
		char centVingtTrois;

		trenteQuatre = 34;
		soixanteCinq = 65;
		centVingtTrois = 123;
	
		System.out.println("Le caractère unicode qui correspond à 34: " + trenteQuatre);
		System.out.println("Le caractère unicode qui correspond à 65: " + soixanteCinq);
		System.out.println("Le caractère unicode qui correspond à 123: " + centVingtTrois);
	
	}

}
